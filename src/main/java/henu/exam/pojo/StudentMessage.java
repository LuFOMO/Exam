package henu.exam.pojo;

public class StudentMessage {
	
	private int totalNum;
	private int loginNum;
	private int unloginNum;
	private int submitNum;
	private int unsumbitNum;

	public int getTotalNum() {
		return totalNum;
	}
	public void setTotalNum(int totalNum) {
		this.totalNum = totalNum;
	}
	public int getLoginNum() {
		return loginNum;
	}
	public void setLoginNum(int loginNum) {
		this.loginNum = loginNum;
	}
	public int getUnloginNum() {
		return unloginNum;
	}
	public void setUnloginNum(int unloginNum) {
		this.unloginNum = unloginNum;
	}
	public int getSubmitNum() {
		return submitNum;
	}
	public void setSubmitNum(int submitNum) {
		this.submitNum = submitNum;
	}
	public int getUnsumbitNum() {
		return unsumbitNum;
	}
	public void setUnsumbitNum(int unsumbitNum) {
		this.unsumbitNum = unsumbitNum;
	}

}
